package com.example.jimappv2a.Model;

public class MateriasModel {
    private String nombre;
    private Long cuatuno, cuatdos;

    public MateriasModel() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Long getCuatuno() {
        return cuatuno;
    }

    public void setCuatuno(Long cuatuno) {
        this.cuatuno = cuatuno;
    }

    public Long getCuatdos() {
        return cuatdos;
    }

    public void setCuatdos(Long cuatdos) {
        this.cuatdos = cuatdos;
    }
}
